    private void generatePDFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generatePDFActionPerformed
        try {
            if (admissionNo.getText().equals("") || fromDate.getText().equals("") || toDate.getText().equals("") || fullName.getText().equals("") ) {
                Utility.showError(this, "Please fill the form generating the certificate.");
                return;
            }
            StudyCertificateParams params = new StudyCertificateParams();
            params.setAdmissionNo(admissionNo.getText().trim());
            params.setCharacter(characterCombo.getSelectedItem().toString());
            params.setFromStd(fromStdCombo.getSelectedItem().toString());
            params.setToStd(toStdCombo.getSelectedItem().toString());
            params.setStartDate(fromDate.getText().toString());
            params.setToDate(toDate.getText().toString());
            params.setName(fullName.getText().toString().toString());
            StudyCertificateGenerator scg = new StudyCertificateGenerator();
            File file = new File(Utility.STUDY_CERTIFICATE_PATH);
            if (!file.exists()) {
                if (file.mkdir()) {
                    // success
                } else {
                    Utility.showError(this, "Error while creating studycertificates directory under C:\\");
                    return;
                }
            }

            String filename=Utility.STUDY_CERTIFICATE_PATH+"\\"+params.getAdmissionNo()+"_"+params.getName().replaceAll(" ", "_")+".pdf";

            scg.createPdf(params,filename);
            
            if (Desktop.isDesktopSupported()) {
                try {
                    File myFile = new File(filename);
                    Desktop.getDesktop().open(myFile);
                } catch (IOException e) {
                    Utility.showError(this, "Cannot open pdf viewer. Install PDF viewer software!");
                }
            }
        } catch (DocumentException|IOException ex) {
           Utility.showError(this, ex.getMessage().toString());
        }


    }//GEN-LAST:event_generatePDFActionPerformed

