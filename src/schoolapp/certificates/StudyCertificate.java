    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        if (admissionNo.getText().equals("")) {
            Utility.showError(this, "Please enter the admission no.!");
            clearForm();
            return;
        }

        Vector<Object> columnNames = new Vector<>();
        Vector<Object> data = new Vector<>();
        //  Connect to an MySQL Database, run query, get result set

        String sql = "SELECT first_name,middle_name,last_name,date_of_birth"
                + " FROM students where admission_no=" + admissionNo.getText() + ";";

        // Java SE 7 has try-with-resources
        // This will ensure that the sql objects are closed when the program 
        // is finished with them
        try {
            Connection connection = Utility.getConnection();
            Statement stmt = (Statement) connection.createStatement();
            ResultSet rs = stmt.executeQuery(sql);

            ResultSetMetaData md = (ResultSetMetaData) rs.getMetaData();
            int columns = md.getColumnCount();

            //  Get column names
            for (int i = 1; i <= columns; i++) {
                columnNames.add(md.getColumnName(i));
            }

            boolean empty = true;
            while (rs.next()) {
                empty = false;
                String firstName = rs.getString(FIRSTNAME);
                String middleName = rs.getString(MIDDLENAME);
                String lastName = rs.getString(LASTNAME);
                String studentFullName = firstName + " " + middleName + " " + lastName;
                fullName.setText(studentFullName);
                break;
            }
            if (empty) {
                Utility.showError(this, "Found no matching admission no. " + admissionNo.getText());
                clearForm();
            }
            Utility.closeConnections(this, connection, stmt, rs);
        } catch (SQLException | ClassNotFoundException e) {
            Utility.showError(this, "Error in DB connection!" + e.toString());
        }

    }//GEN-LAST:event_searchActionPerformed

