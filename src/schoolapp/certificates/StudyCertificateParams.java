package schoolapp.certificates;

/**
 *
 * @author prohit
 */
public class StudyCertificateParams {
    private String admissionNo;
    private String name;
    private String character;
    private String startDate;
    private String toDate;
    private String fromStd;
    private String toStd;

  


    public StudyCertificateParams() {
    }

    public String getAdmissionNo() {
        return admissionNo;
    }

    public void setAdmissionNo(String admissionNo) {
        this.admissionNo = admissionNo;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

  

    public String getCharacter() {
        return character;
    }

    public void setCharacter(String character) {
        this.character = character;
    }

    public String getStartDate() {
        return startDate;
    }

    public void setStartDate(String startDate) {
        this.startDate = startDate;
    }

    public String getToDate() {
        return toDate;
    }

    public void setToDate(String toDate) {
        this.toDate = toDate;
    }

    public String getFromStd() {
        return fromStd;
    }

    public void setFromStd(String fromStd) {
        this.fromStd = fromStd;
    }

    public String getToStd() {
        return toStd;
    }

    public void setToStd(String toStd) {
        this.toStd = toStd;
    }
    
           
}
